import pandas as pd

# =====================================================
# 1. ЗАГРУЖАЕМ СЫРЫЕ ДАННЫЕ
# =====================================================

df = pd.read_csv("data/raw/car_listings.csv")

print("Количество строк до очистки:", len(df))


# =====================================================
# 2. ПРИВОДИМ ЧИСЛОВЫЕ ПОЛЯ К ЧИСЛАМ
# =====================================================

# Если оставить их строками, нельзя будет считать средние цены,
# медиану, строить графики и выполнять SQL-анализ.

df["price"] = pd.to_numeric(df["price"], errors="coerce")
df["year"] = pd.to_numeric(df["year"], errors="coerce")
df["mileage"] = pd.to_numeric(df["mileage"], errors="coerce")
df["power_hp"] = pd.to_numeric(df["power_hp"], errors="coerce")


# =====================================================
# 3. УДАЛЯЕМ ОБЪЯВЛЕНИЯ БЕЗ ЦЕНЫ
# =====================================================

# Например:
# Ferrari Ankauf -> цена отсутствует

df = df[df["price"].notna()]


# =====================================================
# 4. УДАЛЯЕМ СЛИШКОМ ДЕШЕВЫЕ ОБЪЯВЛЕНИЯ
# =====================================================

# Запчасти часто стоят:
# 50 €
# 90 €
# 120 €
#
# Настоящие автомобили обычно дороже.

df = df[df["price"] >= 500]


# =====================================================
# 5. УДАЛЯЕМ ПРОБЕГ 0
# =====================================================

# Обычно это ошибка объявления.

df = df[df["mileage"] > 0]


# =====================================================
# 6. УДАЛЯЕМ НЕРЕАЛИСТИЧНЫЕ ГОДА
# =====================================================

# Не может быть автомобиля 2035 года.

df = df[df["year"] >= 1980]
df = df[df["year"] <= 2026]


# =====================================================
# 7. СОЗДАЁМ ВОЗРАСТ АВТОМОБИЛЯ
# =====================================================

# Одна из важнейших аналитических метрик.

CURRENT_YEAR = 2026

df["vehicle_age"] = CURRENT_YEAR - df["year"]


# =====================================================
# 8. ЦЕНА ЗА 1000 КМ
# =====================================================

# Позже будем использовать для поиска выгодных предложений.

df["price_per_1000km"] = (
    df["price"] / (df["mileage"] / 1000)
)


# =====================================================
# 9. ЦЕНА ЗА 1 Л.С.
# =====================================================

# Интересная метрика для сравнения автомобилей.

df["price_per_hp"] = (
    df["price"] / df["power_hp"]
)

# Убираем деление на ноль и пустые значения

df["price_per_hp"] = df["price_per_hp"].replace(
    [float("inf"), -float("inf")],
    pd.NA
)


# =====================================================
# 10. УДАЛЯЕМ ДУБЛИКАТЫ
# =====================================================

df = df.drop_duplicates()

# =====================================================
# 11. УДАЛЯЕМ НЕРЕАЛИСТИЧНЫЙ ПРОБЕГ
# =====================================================

# Машина не может иметь пробег 50 км
# если ей 10-20 лет

df = df[
    (df["mileage"] >= 1000)
]

# =====================================================
# 11. СОХРАНЯЕМ ОЧИЩЕННЫЕ ДАННЫЕ
# =====================================================

df.to_csv(
    "data/processed/car_listings_clean.csv",
    index=False,
    encoding="utf-8-sig"
)

# =====================================================
# OPPORTUNITY SCORE
# =====================================================

price_max = df["price"].max()
mileage_max = df["mileage"].max()
age_max = df["vehicle_age"].max()

df["opportunity_score"] = (

    (1 - df["price"] / price_max) * 40 +

    (1 - df["mileage"] / mileage_max) * 30 +

    (1 - df["vehicle_age"] / age_max) * 30

)

df["opportunity_score"] = (
    df["opportunity_score"]
    .round(0)
    .astype(int)
)

print("Количество строк после очистки:", len(df))

print("\nПервые 5 строк:")

print(df.head())